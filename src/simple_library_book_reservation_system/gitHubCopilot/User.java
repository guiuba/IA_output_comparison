package simple_library_book_reservation_system.gitHubCopilot;

public class User {
    private String name;
    private String userId;

    public User(String name, String userId) {
        this.name = name;
        this.userId = userId;
    }

    // Getter methods
    public String getName() {
        return name;
    }

    public String getUserId() {
        return userId;
    }
}
